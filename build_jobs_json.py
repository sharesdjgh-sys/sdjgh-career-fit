def parse_yaml_block(text):
    """파일 끝 ```yaml ... ``` 블록 파싱"""
    m = re.search(r'```yaml\s*\n(.*?)```', text, re.DOTALL)
    if not m:
        return {}
    try:
        return yaml.safe_load(m.group(1)) or {}
    except Exception:
        return {}

def assign_holland(job_name, job_type, category):
    for keywords, codes in HOLLAND_RULES:
        if any(k in job_name or k in job_type for k in keywords):
            return codes
    return CATEGORY_HOLLAND.get(category, ["I", "R"])

def assign_track(job_name, job_type, category):
    for keywords, track in TRACK_RULES:
        if any(k in job_name or k in job_type for k in keywords):
            return track
    return CATEGORY_TRACK.get(category, "문과")

def assign_subjects(job_name, category):
    for kw, subjs in SUBJECT_OVERRIDES.items():
        if kw in job_name:
            return subjs
    return CATEGORY_SUBJECTS.get(category, SUBJECT_MAP["문과_일반"])

def assign_aptitude(holland):
    primary = holland[0] if holland else "I"
    secondary = holland[1] if len(holland) > 1 else "C"
    seen = []
    for code in [primary, secondary]:
        for factor in APTITUDE_MAP.get(code, []):
            if factor not in seen:
                seen.append(factor)
    return seen[:4]

def norm_level(v):
    mapping = {"HGH": "높음", "MID": "보통", "LOW": "낮음",
               "HIGH": "높음", "LOWER": "낮음", "UPPER": "높음", "": "정보없음"}
    return mapping.get(str(v).upper(), str(v)) if v else "정보없음"

def parse_salary_from_text(text):
    """본문에서 연봉 파싱 (avg_salary_10k 없을 때)"""
    m = re.search(r'평균연봉[^0-9]*([0-9,]+)만원', text)
    if m:
        try:
            return int(m.group(1).replace(',', ''))
        except:
            pass
    return None

